class Endereco
{
    private string cidade;
    private string bairro;
    private int numero;
    
    public string Cidade
    {
        get { return cidade; }
        set { cidade = value; }
    }

    public string Bairro
    {
        get { return bairro; }
        set { bairro = value; }
    }

    public int numero
    {
        get { return numero;}
        set { numero = value; }
    }
}