    private void preencherTabela(ArrayList<Material> mats) {

        DefaultTableModel dftm = (DefaultTableModel) jTMateriais.getModel();
        
        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }
        
        for(Material m : mats){
            Object[] rowData = {
                m.getId(),
                m.getTitulo(),
                m.getConteudo()
            };
            dftm.addRow(rowData);
        }
    }
   
    private void calcularEMostrarMediaAvaliacoes() throws ClassNotFoundException {
        try {
            AvaliacaoDAO avaliacaoDAO = new AvaliacaoDAO();
            ArrayList<Integer> avaliacoes = avaliacaoDAO.obterAvaliacoesPorProfessor(professorLogado.getId());
            
            if (avaliacoes.isEmpty()) {
                jlMedia.setText("N/A");
                return;
            }
            
            int soma = 0;
            for (int nota : avaliacoes) {
                soma += nota;
            }
            
            double media = (double) soma / avaliacoes.size();
            jlMedia.setText(String.format("%.2f", media));
        } catch (SQLException ex) {
            ex.printStackTrace();
            jlMedia.setText("Erro");
        }
    }
    
