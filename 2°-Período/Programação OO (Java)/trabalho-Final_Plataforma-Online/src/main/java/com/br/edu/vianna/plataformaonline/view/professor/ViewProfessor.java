    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int row = jTMateriais.getSelectedRow();
        if(row <= -1 ){
            JOptionPane.showMessageDialog(null, "Selecione um Material!");
        }else{

            ViewDetailMateriais det = new ViewDetailMateriais(null, true);

            det.preparedEdit(mats.get(row));
            det.setVisible(true);

            formWindowOpened(null);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int row = jTMateriais.getSelectedRow();
        if(row <= -1 ){
            JOptionPane.showMessageDialog(null, "Selecione um material!");
        }else{

            if(JOptionPane.showConfirmDialog(null, "Deseja apagar o material " +mats.get(row).getTitulo()+ "?", "Apagar Material?", JOptionPane.YES_NO_OPTION)
                == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "Apagou!");

                try {
                    new MaterialDAO().apagar(mats.get(row));

                    formWindowOpened(null);

                } catch ( Exception e ) {
                    JOptionPane.showMessageDialog(null, "Não foi possivel apagar o curso!");
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

