    private void preencherTabela(ArrayList<Curso> cursos) {
        
        DefaultTableModel dftm = (DefaultTableModel) jTCursos.getModel();
        
        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }
        
        for (Curso c : cursos) {
            Object[] rowData = {
                c.getId(),
                c.getNome(),
                c.getDescricao()
            };
            dftm.addRow(rowData);
        }
    }
    
    private void inscreverAlunoNoCurso(int alunoId, int cursoId) {
        try {
            boolean sucesso = new CursoDAO().inscreverAlunoNoCurso(alunoId, cursoId);
            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Inscrição realizada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível realizar a inscrição.");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao realizar inscrição: " + ex.getMessage());
        }
    }

    
