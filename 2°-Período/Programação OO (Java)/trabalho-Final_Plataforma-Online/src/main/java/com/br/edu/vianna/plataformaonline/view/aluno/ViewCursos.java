    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String nomeCurso = jText.getText(); 
            if (nomeCurso.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite um nome de curso para buscar.");
                return;
            }

            ArrayList<Curso> cursos = new CursoDAO().buscarCursoPorNome(jText.getText());
            preencherTabela(cursos);

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel buscar os cursos!");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

