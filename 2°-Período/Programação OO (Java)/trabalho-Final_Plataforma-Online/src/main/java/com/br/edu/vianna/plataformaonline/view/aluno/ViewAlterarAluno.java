    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            int id = Integer.parseInt(jlId.getText()); 
            String nome = jtfNome.getText();
            String email = jtfEmail.getText();
            String login = jtfLogin.getText();
            String senha = new String(jlSenha.getText());
            String matricula = jlMatricula.getText();

            Aluno alunoAtualizado = new Aluno(id, nome, email, login, senha, matricula);
            
            alunoAtualizado.setId(aluno.getId());
            new AlunoDAO().alterar(alunoAtualizado);

            JOptionPane.showMessageDialog(null, "Dados atualizado com sucesso!");
            setVisible(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel atualizar os dados do aluno, dados incorretos!");
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

